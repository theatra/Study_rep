# Задаємо послідовність елементів списку:

list = [2, 28, 14, 9, 0]

# Виводимо для наочності:
print(list)

# Додаємо умову, аби працювало з порожнім списком:
if (len(list)) == 0:
    print("[]")

# В інакшому випадку міняємо крайні елементи місцями + видаляємо останній:
elif (len(list)) > 0:
    list[0] = list[-1]
    del list[-1]
    print(list)
